    public static List<string> GetPalette(uint requiredNumberofColors)
    {
        if (requiredNumberofColors <= standardColors.Count / 2)
        {
            return GetStandardColors(requiredNumberofColors);
        }

        if (requiredNumberofColors <= standardColors.Count)
        {
            return standardColors;
        }

        if (requiredNumberofColors <= 1)
        {
            return Palette0001;
        }

        if (requiredNumberofColors <= 2)
        {
            return Palette0002;
        }

        if (requiredNumberofColors <= 4)
        {
            return Palette0004;
        }

        if (requiredNumberofColors <= 8)
        {
            return Palette0008;
        }

        if (requiredNumberofColors <= 16)
        {
            return Palette0016;
        }

        if (requiredNumberofColors <= 32)
        {
            return Palette0032;
        }

        if (requiredNumberofColors <= 64)
        {
            return Palette0064;
        }

        if (requiredNumberofColors <= 128)
        {
            return Palette0128;
        }

        if (requiredNumberofColors <= 256)
        {
            return Palette0256;
        }

        if (requiredNumberofColors <= 512)
        {
            return Palette0512;
        }

        if (requiredNumberofColors <= 1024)
        {
            return Palette1024;
        }

        if (requiredNumberofColors <= 2048)
        {
            return Palette2048;
        }

        return Palette4096;
    }

    public static string GetRandomLightColor()
    {
        // var min = 229;
        const int min = 235;
        var r = Math.Floor((256 - min) * _random.NextDouble()) + min + 1;
        var g = Math.Floor((256 - min) * _random.NextDouble()) + min + 1;
        var b = Math.Floor((256 - min) * _random.NextDouble()) + min + 1;

        return $"#{(int)r:x2}{(int)g:x2}{(int)b:x2}";
    }

    private static List<string> GetStandardColors(uint n)
    {
        var max = standardColors.Count - 1;
        var random = new Random();
        var numbers = new HashSet<int>();
        while (numbers.Count < n) numbers.Add(random.Next(0, max));
        var result = new List<string>();
        foreach (var i in numbers)
        {
            result.Add(standardColors[i]);
        }

        return result;
    }
